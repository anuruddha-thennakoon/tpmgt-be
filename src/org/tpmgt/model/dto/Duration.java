package org.tpmgt.model.dto;

public class Duration {
	
	private int days;
	private int months;
	private int years;
	
	public Duration() {

	}

	public int getDays() {
		return days;
	}

	public void setDays(int days) {
		this.days = days;
	}

	public int getMonths() {
		return months;
	}

	public void setMonths(int months) {
		this.months = months;
	}

	public int getYears() {
		return years;
	}

	public void setYears(int years) {
		this.years = years;
	}
	
	

}
